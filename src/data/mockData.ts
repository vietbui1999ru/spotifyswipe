// Helper function to calculate top genres from songs
function calculateTopGenres(songs: Song[]): Genre[] {
  const genreCount = new Map<Genre, number>();

  songs.forEach(song => {
    song.genres.forEach(genre => {
      genreCount.set(genre, (genreCount.get(genre) || 0) + 1);
    });
  });

  return Array.from(genreCount.entries())
    .sort((a, b) => b[1] - a[1])
    .slice(0, 3)
    .map(([genre]) => genre);
}

